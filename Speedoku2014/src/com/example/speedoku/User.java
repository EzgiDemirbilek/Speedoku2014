package com.example.speedoku;

public class User {
	
	private String player;

	public String getPlayer() {
		return player;
	}

	public void setPlayer(String player) {
		this.player = player;
	}
	
	
	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}


	public int getPoints() {
		return points;
	}

	public void setPoints(int points) {
		this.points = points;
	}


	private int time;
	
	private int points;
	

}
